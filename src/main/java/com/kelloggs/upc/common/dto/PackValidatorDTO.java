package com.kelloggs.upc.common.dto;

import java.io.Serializable;
import java.util.Date;

import javax.xml.bind.annotation.XmlRootElement;

import com.thoughtworks.xstream.annotations.XStreamAlias;


/**
 * The Class PackValidatorDTO.
 */
@XmlRootElement(name = "PackCodeValidator", namespace = "com.kelloggs.upc.common.dto")
@XStreamAlias("PackCodeValidator")
public class PackValidatorDTO implements Serializable {

	/**
	 * Serialization version Id
	 */
	private static final long serialVersionUID = 1L;

	/** The promotion name. */
	private String promotionName;

	/** The pack code set name. */
	private String packCodeSetName;

	/** The start date. */
	private Date startDate;

	/** The end date. */
	private Date endDate;

	/** The valid status. */
	private boolean validStatus;

	/** The redem status. */
	private int redemStatus;

	/**
	 * Gets the promotion name.
	 *
	 * @return the promotion name
	 */
	public String getPromotionName() {
		return promotionName;
	}

	/**
	 * Sets the promotion name.
	 *
	 * @param iPromotionName the new promotion name
	 */
	public void setPromotionName(final String iPromotionName) {
		this.promotionName = iPromotionName;
	}

	/**
	 * Gets the pack code set name.
	 *
	 * @return the pack code set name
	 */
	public String getPackCodeSetName() {
		return packCodeSetName;
	}

	/**
	 * Sets the pack code set name.
	 *
	 * @param iPackCodeSetName the new pack code set name
	 */
	public void setPackCodeSetName(final String iPackCodeSetName) {
		this.packCodeSetName = iPackCodeSetName;
	}

	/**
	 * Gets the start date.
	 *
	 * @return the start date
	 */
	public Date getStartDate() {
		return startDate;
	}

	/**
	 * Sets the start date.
	 *
	 * @param iStartDate the new start date
	 */
	public void setStartDate(final Date iStartDate) {
		this.startDate = iStartDate;
	}

	/**
	 * Gets the end date.
	 *
	 * @return the end date
	 */
	public Date getEndDate() {
		return endDate;
	}

	/**
	 * Sets the end date.
	 *
	 * @param iEndDate the new end date
	 */
	public void setEndDate(final Date iEndDate) {
		this.endDate = iEndDate;
	}

	/**
	 * Checks if is valid status.
	 *
	 * @return true, if is valid status
	 */
	public boolean isValidStatus() {
		return validStatus;
	}

	/**
	 * Sets the valid status.
	 *
	 * @param iValidStatus the new valid status
	 */
	public void setValidStatus(final boolean iValidStatus) {
		
		this.validStatus = iValidStatus;
	}

	/**
	 * Gets the redem status.
	 *
	 * @return the redem status
	 */
	public int getRedemStatus() {
		return redemStatus;
	}

	/**
	 * Sets the redem status.
	 *
	 * @param iRedemStatus the new redem status
	 */
	public void setRedemStatus(final int iRedemStatus) {
		this.redemStatus = iRedemStatus;
	}

	

}
